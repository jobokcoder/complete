5.Complete계정 웹사이트: 회원이 온라인을 통해 Complete계정 정보를 조회 및 수정할 수 있는 인터넷 사이트를 말합니다.<br>
6.Complete계정 정보 : Complete계정을 이용하기 위해 회사가 정한 필수 내지 선택 입력 정보로서 Complete계정 웹사이트 또는 개별 서비스 내 Complete계정 설정 화면을 통해 정보 확인, 변경 처리 등을 관리할 수 있는 회원정보 항목을 말합니다.<br>
