7.회사로부터 회원자격정지 조치 등을 받은 회원이 그 조치기간에 이용계약을 임의로 해지하고 재이용을 신청하는 경우<br>
8.기타 관련법령에 위배되거나 세부지침 등 회사가 정한 기준에 반하는 경우<br>
②만약, 여러분이 위 조건에 위반하여 Complete계정을 생성한 것으로 판명된 때에는 회사는 즉시 여러분의 Complete계정 이용을 정지시키거나 Complete계정을 삭제하는 등 적절한 제한을 할 수 있습니다.<br>
제 3 장 Complete계정 이용<br>
제 7 조 (Complete계정 제공)<br>
①회사가 개별 서비스와 연동하여 Complete계정에서 제공하는 서비스(이하 “Complete계정 서비스” 또는 “서비스”) 내용은 아래와 같습니다.<br>
1.통합로그인 : Complete계정이 적용된 개별 서비스에서 하나의 Complete계정과 비밀번호로 로그인할 수 있는 통합 회원 인증 서비스를 이용할 수 있습니다.<br>
2.SSO(Single Sign On): 웹브라우저나 특정 모바일 기기에서 Complete계정 1회 로그인으로 여러분이 이용 중인 개별 서비스간 추가 로그인 없이 자동 접속 서비스를 이용할 수 있습니다.<br>
3.Complete계정 정보 통합 관리 : 개별 서비스 이용을 위해 Complete계정 정보를 통합 관리합니다. 또한, 여러분이 이용하고자 하는 개별 서비스의 유형에 따라 전문기관을 통한 실명확인 및 본인인증을 요청할 수 있고, 이를 Complete계정 정보로 저장합니다.<br>
4.인증서비스 : 회사가 제공하는 전자서명과 인증서를 활용한 일체의 서비스를 말합니다.<br>
5.기타 회사가 제공하는 서비스<br>
②회사는 더 나은 Complete계정 서비스의 제공을 위하여 여러분에게 서비스의 이용과 관련된 각종 고지, 관리 메시지 및 기타 광고를 비롯한 다양한 정보를 서비스화면 내에 표시하거나 여러분의 이메일로 전송할 수 있습니다. 광고성 정보 전송의 경우에는 사전에 수신에 동의한 경우에만 전송합니다.<br>
제 8 조 (Complete계정 서비스의 변경 및 종료)<br>
①회사는 Complete계정 서비스를 365일, 24시간 쉬지 않고 제공하기 위하여 최선의 노력을 다합니다. 다만, 아래 각 호의 경우 Complete계정 서비스의 전부 또는 일부를 제한하거나 중지할 수 있습니다.<br>
1.Complete계정 서비스용 설비의 유지·보수 등을 위한 정기 또는 임시 점검의 경우<br>
2.정전, 제반 설비의 장애 또는 이용량의 폭주 등으로 정상적인 Complete계정 이용에 지장이 있는 경우<br>
3.관계사와의 계약 종료, 정부의 명령/규제 등 회사의 제반 사정으로 Complete계정 서비스를 유지할 수 없는 경우<br>
4.기타 천재지변, 국가비상사태 등 불가항력적 사유가 있는 경우<br>
②전항에 의한 Complete계정 서비스 중단의 경우에는 미리 제12조에서 정한 방법으로 여러분에게 통지 내지 공지하겠습니다. 다만, 회사로서도 예측할 수 없거나 통제할 수 없는 사유(회사의 과실이 없는 디스크 내지 서버 장애, 시스템 다운 등)로 인해 사전 통지 내지 공지가 불가능한 경우에는 그러하지 아니합니다. 이러한 경우에도 회사가 상황을 파악하는 즉시 최대한 빠른 시일 내에 서비스를 복구하도록 노력하겠습니다.<br>
③여러분에게 중대한 영향을 미치는 서비스의 변경 사항이나 종료는 Complete계정에 등록된 이메일 주소로 이메일(이메일주소가 없는 경우 서비스 내 전자쪽지 발송, 서비스 내 알림 메시지를 띄우는 등의 별도의 전자적 수단) 발송 또는 여러분이 등록한 휴대폰번호로 Complete톡 메시지 또는 문자메시지 발송하는 방법 등으로 개별적으로 알려 드리겠습니다.<br>
제 9 조 (Complete계정 관리)<br>
①Complete계정은 여러분 본인만 이용할 수 있으며, 다른 사람이 여러분의 Complete계정을 이용하도록 허락할 수 없습니다. 그리고 여러분은 다른 사람이 여러분의 Complete계정을 무단으로 사용할 수 없도록 직접 비밀번호를 관리하여야 합니다. 회사는 다른 사람이 여러분의 Complete계정을 무단으로 사용하는 것을 막기 위하여 비밀번호 입력 및 추가적인 본인 확인 절차를 거치도록 할 수 있습니다. 만약 무단 사용이 발견된다면, 고객센터를 통하여 회사에게 알려주시기 바라며, 회사는 무단 사용을 막기 위한 방법을 여러분에게 안내하도록 하겠습니다.<br>
②여러분은 Complete계정 웹사이트 또는 개별 서비스 내 Complete계정 설정 화면을 통하여 여러분의 Complete계정 정보를 열람하고 수정할 수 있습니다. 다만, Complete계정 서비스의 제공 및 관리를 위해 필요한 Complete계정, 전화번호, 단말기 식별번호, 기타 본인확인정보 등 일부 정보는 수정이 불가능할 수 있으며, 수정하는 경우에는 추가적인 본인 확인 절차가 필요할 수 있습니다. 여러분이 이용 신청 시 알려주신 내용에 변동이 있을 때, 직접 수정하시거나 이메일, 고객센터를 통하여 회사에 알려 주시기 바랍니다.<br>
제 10 조 (인증서비스)<br>
①회사는 전자서명생성정보 및 인증서를 발급하고, 전자서명과 인증서를 활용한 각종 서비스를 아래 각 호와 같이 인증회원에게 제공합니다. 이 때 회사는 필요한 경우 인증서비스의 유형 및 종류를 추가하거나, 부가서비스를 별도로 제공할 수 있습니다.<br>
1.전자서명생성정보 및 인증서 발급<br>
2.전자서명 및 인증서를 활용한 각종 서비스<br>
3.이용기관 로그인 및 신원확인을 위한 간편인증<br>
